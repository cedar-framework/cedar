SUBROUTINE BMG2_SymStd_COPY_cg_WS_RHS( &
  &                Q_ws, NLx, NLy, &
  &                Q, NGxg, NGyg, &
  &                iGs, jGs &
  &                )

  ! ==========================================================================
  !  --------------------
  !   DESCRIPTION:
  !  --------------------
  !
  !   Copy the RHS for the coarse-grid BoxMG solve.
  !
  ! =======================================================================
  ! $license_flag$
  ! =======================================================================
  !  --------------------
  !   INPUT:
  !  --------------------
  !
  !
  ! =======================================================================
  !  --------------------
  !   OUTPUT:
  !  --------------------
  !
  !
  !
  ! =======================================================================
  !  --------------------
  !   LOCAL:
  !  --------------------
  !
  !
  ! ==========================================================================

  IMPLICIT NONE

  ! ----------------------------
  !     Includes
  !
  INCLUDE 'BMG_stencils_f90.h'

  ! ----------------------------
  !     Argument Declarations
  !
  !
  !  Global/Local indexing
  !
  INTEGER NLx, NLy, &
  &        NGxg, NGyg, &
  &        iGs, jGs

  !
  !  Solution: SER
  !
  REAL*8   Q(NGxg,NGyg)
  !
  !  Solution: WS
  !
  REAL*8   Q_ws(NLx,NLy)

  ! ----------------------------
  !     Local Declarations
  !
  INTEGER  iG, iL, jG, jL

  ! ==========================================================================

  DO jL=1, NLy
     jG = jGs + jL
     DO iL=1, NLx
        iG = iGs + iL
        Q(iG,jG) = Q_ws(iL,jL)
     ENDDO
  ENDDO

  ! ==========================================================================

  RETURN
END SUBROUTINE BMG2_SymStd_COPY_cg_WS_RHS
