      SUBROUTINE BMG2_SymStd_SETUP_recip( &
     &                SO, SOR, Nx, Ny, NStncl&
     &                ) BIND(C, NAME='MPI_BMG2_SymStd_SETUP_recip')

! ======================================================================
!  --------------------
!   DESCRIPTION:
!  --------------------
!
!     BMG2_SymStd_cons_recip computes the reciprocal of the central
!     stencil coefficient.
!
! ======================================================================
! $license_flag$
! ======================================================================
!  --------------------
!   INPUT:
!  --------------------
!
!
! ======================================================================
!  --------------------
!   INPUT/OUTPUT:
!  --------------------
!
!
! ======================================================================
!  --------------------
!   OUTPUT:
!  --------------------
!
!
! ======================================================================
!  --------------------
!   LOCAL:
!  --------------------
!
!
! ======================================================================
      USE ModInterface
      IMPLICIT NONE

! -----------------------------
!     Includes
!
      INCLUDE 'BMG_constants_f90.h'
      INCLUDE 'BMG_stencils_f90.h'

! ---------------------------
!    Argument Declarations:
!
      INTEGER(C_INT), VALUE :: NStncl
      INTEGER(len_t), VALUE :: Nx, Ny
      REAL(real_t) :: SO(Nx+1,Ny+1,NStncl), SOR(Nx,Ny,*)

! --------------------------
!     Local Declarations:
!
      INTEGER   i, j

! ======================================================================

      DO j = 2, Ny-1
         DO i = 2, Nx-1
            SOR(i,j,msor)=1.0/SO(i,j,ko)
         ENDDO
      ENDDO

! ======================================================================

      RETURN
      END
